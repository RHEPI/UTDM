class TimestepEmbedder(nn.Module):
    def __init__(self, hidden_size, frequency_embedding_size=256):
        super().__init__()
        self.mlp = nn.Sequential(
            nn.Linear(frequency_embedding_size, hidden_size, bias=True),
            nn.SiLU(),
            nn.Linear(hidden_size, hidden_size, bias=True),
        )
        self.frequency_embedding_size = frequency_embedding_size

    @staticmethod
    def timestep_embedding(t, dim, max_period=10000):
        half = dim // 2
        freqs = torch.exp(
            -math.log(max_period) * torch.arange(start=0, end=half, dtype=torch.float32) / half
        ).to(device=t.device)
        args = t[:, None].float() * freqs[None]
        embedding = torch.cat([torch.cos(args), torch.sin(args)], dim=-1)
        if dim % 2:
            embedding = torch.cat([embedding, torch.zeros_like(embedding[:, :1])], dim=-1)
        return embedding

    def forward(self, t):
        t_freq = self.timestep_embedding(t, self.frequency_embedding_size)
        t_emb = self.mlp(t_freq)
        return t_emb

class Contextual_Fusion_Attention(nn.Module):
    def __init__(self,
                 dim,
                 qkv_bias=False,
                 attn_drop_ratio=0.,
                 proj_drop_ratio=0.):
        super(Contextual_Fusion_Attention, self).__init__()
        self.scale = dim ** -0.5
        self.qkv = nn.Linear(dim, dim * 3, bias=qkv_bias)
        self.attn_drop = nn.Dropout(attn_drop_ratio)
        self.proj = nn.Linear(dim, dim)
        self.proj_drop = nn.Dropout(proj_drop_ratio)
        self.fusion = nn.Linear(dim * 2, dim)

    def forward(self, x):
        # [B, T, D]
        B, N, C = x.shape

        # qkv: -> [3, N, T, D]
        qkv = self.qkv(x).reshape(B, N, 3, C).permute(2,0,1,3)
        # q,k1,v: -> [N, T, D]
        q, k1, v = qkv[0], qkv[1], qkv[2]
        # y: -> [N, T, 2D]
        y1 = torch.cat((k1,x), dim=-1)
        # y: -> [N, T, D]
        y2 = self.fusion(y1)
        # Compute attention weights
        att = torch.matmul(q, y2.transpose(-2, -1)) * self.scale  # [B, T, T]
        att = F.softmax(att, dim=-1)  # [B, T, T]

        # Compute k2
        k2 = torch.matmul(att, v)  # [B, T, D]

        return k1+k2

# input:[N, T, D] output:[N, T, D]
class Cascaded_Contextual_Fusion_Attention(nn.Module):
    def __init__(self, dim, num_heads=12):
        super().__init__()
        self.num_heads = num_heads
        self.dim = dim
        self.cot_attn = Contextual_Fusion_Attention(dim=self.dim // self.num_heads)
        self.proj = nn.Sequential(
            nn.ReLU(),
            nn.Conv1d(dim, dim, kernel_size=1, stride=1, padding=0)
        )

    def forward(self, x):  # x (N(B), T, D)
        B, T, D = x.shape
        feats_in = x.chunk(self.num_heads, dim=2)
        feats_out = []
        feat = feats_in[0]
        for i in range(self.num_heads):
            if i > 0:
                feat = feat + feats_in[i]
            feat = self.cot_attn(feat)
            feats_out.append(feat)
        x = torch.cat(feats_out, 2)
        x = x.transpose(1, 2)  # [B, D, T]
        x = self.proj(x)
        x = x.transpose(1, 2)
        return x


